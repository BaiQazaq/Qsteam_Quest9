# 2. What is the probability of flipping a coin and getting heads?

# The probability of flipping a fair coin and getting heads is 0.5 or 50%. In a fair coin toss, 
# there are two possible outcomes: heads or tails. Since each outcome is equally likely, 
# the probability of getting heads is 1 divided by the total number of possible outcomes (2), which equals 0.5. 
# Therefore, the probability of getting heads on a fair coin flip is 0.5 or 50%.

# Вероятность выпадения орла при броске честной монеты составляет 0,5 или 50%. 
# При честном броске монеты есть два возможных исхода: орел или решка. 
# Поскольку каждый из этих исходов равновероятен, вероятность выпадения орла равна 1 делённое на общее количество возможных исходов (2), 
# что равно 0,5. Таким образом, вероятность выпадения орла при честном броске монеты составляет 0,5 или 50%.

# Define the number of favorable outcomes (getting heads)
favorable_outcomes = 1
# Define the total number of possible outcomes (heads or tails)
