# 1. Calculate the probability of rolling a 4 on a standard six-sided die.

# Число благоприятных исходов
favorable_outcomes = 1
# Общее число возможных исходов
possible_outcomes = 6
# Расчет вероятности
probability = favorable_outcomes / possible_outcomes
print("Вероятность выпадения 4 на шестигранной кости:", probability)
